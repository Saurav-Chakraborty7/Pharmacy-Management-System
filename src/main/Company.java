    private void idcomboPopupMenuWillBecomeInvisible(javax.swing.event.PopupMenuEvent evt) {//GEN-FIRST:event_idcomboPopupMenuWillBecomeInvisible
        String item = (String) idcombo.getSelectedItem();
        String sql = "select * from company where ID=?";

        try {
            pre = con.prepareStatement(sql);
            pre.setString(1, item);
            res = pre.executeQuery();
            if (res.next()) {
                name.setText(res.getString("NAME"));
                adres.setText(res.getString("Address"));
                phn.setText(res.getString("phone"));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_idcomboPopupMenuWillBecomeInvisible

