        addcombtn.setVisible(true);
        delcombtn.setVisible(true);
        back.setVisible(true);
        Statement statement = null;
        try {
            statement = con.createStatement();
        } catch (SQLException e) {
            System.out.println("Failed to create statement: " + e.getMessage());
        }
        String id = idtex.getText();
        String name = nametex.getText();
        String adres = addrtex.getText();
        String ph = phntex.getText();
        String sql = "insert into company(ID,NAME,Address,phone) values('" + id + "','" + name + "','" + adres + "','" + ph + "')";
